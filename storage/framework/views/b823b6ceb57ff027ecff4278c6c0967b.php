            try {
                const audioContext = new (window.AudioContext || window.webkitAudioContext)();
                this.beepAudio = audioContext;
            } catch (e) {
                this.beepAudio = null;
            }
        },
        
        activateAudio() {
            if (this.beepAudio && this.beepAudio.state === 'suspended') {
                this.beepAudio.resume();
            }
        },
        
