                this.track = null;
            }
            if (this.stream) {
                this.stream.getTracks().forEach(t => t.stop());
                this.stream = null;
            }
            if (this.zxingReader) {
                try { 
                    this.zxingReader.reset(); 
                    this.zxingReader = null;
                } catch (e) {
                    //console.log('Error resetting ZXing:', e);
                }
            }
            // Clear video element
            const video = this.$refs.video;
            if (video) {
                video.srcObject = null;
            }
            // Reset scan counts
            this.scanCounts = {};
            this.lastCode = '';
            //console.log('Scanner stopped');
