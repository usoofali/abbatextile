            //console.log('Playing beep sound...');
            try {
                if (!this.beepAudio) {
                    this.initBeep();
                }
                if (this.beepAudio) {
                    if (this.beepAudio.state === 'suspended') {
                        this.beepAudio.resume().then(() => {
                            this.createBeepSound();
                        });
                    } else {
                        this.createBeepSound();
                    }
                } else {
                    this.systemBeep();
                }
            } catch (e) {
                //console.log('Beep failed:', e);
                this.systemBeep();
            }
        },
        
